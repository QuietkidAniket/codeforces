byte sbox_lookup(byte x) { return sbox[x >> 4][x & 0x0F]; }

void print_state(byte s[4][4]) {
    for (int i = 0; i < 4; i++) {
        for (int j = 0; j < 4; j++) {
            cout << hex << setw(2) << setfill('0') << (int)s[i][j] << " ";
        }
        cout << "\n";
    }
    cout << dec << "\n";
}

void sub_bytes(byte s[4][4]) {
    for (int i = 0; i < 4; i++)
        for (int j = 0; j < 4; j++)
            s[i][j] = sbox_lookup(s[i][j]);
}

void shift_rows(byte s[4][4]) {
    for (int i = 1; i < 4; i++) {
        byte tmp[4];
        for (int j = 0; j < 4; j++) tmp[j] = s[i][(j + i) % 4];
        for (int j = 0; j < 4; j++) s[i][j] = tmp[j];
    }
}

byte xtime(byte x) { return (x << 1) ^ ((x & 0x80) ? 0x1b : 0x00); }

void mix_columns(byte s[4][4]) {
    for (int j = 0; j < 4; j++) {
        byte a = s[0][j], b = s[1][j], c = s[2][j], d = s[3][j];
        s[0][j] = xtime(a) ^ (xtime(b) ^ b) ^ c ^ d;
        s[1][j] = a ^ xtime(b) ^ (xtime(c) ^ c) ^ d;
        s[2][j] = a ^ b ^ xtime(c) ^ (xtime(d) ^ d);
        s[3][j] = (xtime(a) ^ a) ^ b ^ c ^ xtime(d);
    }
}

void Solve() {
    byte a[4][4] = {
        {0x01, 0x02, 0x03, 0x04},
        {0x11, 0x12, 0x13, 0x14},
        {0x21, 0x22, 0x23, 0x24},
        {0x31, 0x32, 0x33, 0x34}
    };

    // key schedule for round 1
    byte k1[4][4];
    byte tmp[4];
    for (int i = 0; i < 4; i++) tmp[i] = a[i][3];

    // RotWord
    byte var = tmp[0];
    for(int i=0; i<3; i++) tmp[i] = tmp[i+1];
    tmp[3] = var;

    // SubWord + Rcon
    for (int i = 0; i < 4; i++) tmp[i] = sbox_lookup(tmp[i]);
    tmp[0] ^= 0x01;

    for (int i = 0; i < 4; i++) k1[i][0] = a[i][0] ^ tmp[i];
    for (int j = 1; j < 4; j++)
        for (int i = 0; i < 4; i++)
            k1[i][j] = a[i][j] ^ k1[i][j - 1];

    string name = "ANIKET";
    byte z[4][4] = {0};
    for (int i = 0; i < (int)name.size() && i < 16; i++)
        z[i % 4][i / 4] = (byte)name[i];

    cout << "Initial State:\n";
    print_state(z);

    sub_bytes(z);
    shift_rows(z);
    mix_columns(z);

    // AddRoundKey
    for (int i = 0; i < 4; i++)
        for (int j = 0; j < 4; j++)
            z[i][j] ^= k1[i][j];

    cout << "After 1 AES Round:\n";
    print_state(z);
}

